let slideIndex = 0;
let currentLightboxIndex = 0;

/* ==========================================================================
   Core Render Logic
   ========================================================================== */
function renderSlideshow() {
    const container = document.getElementById("dynamicSlideshowContainer");
    if (!container) return;

    container.innerHTML = "";
    portfolioItems.forEach((file, index) => {
        const slideDiv = document.createElement("div");
        slideDiv.className = "mySlides fade";
        slideDiv.style.display = index === 0 ? "block" : "none";

        const isVideo = file.toLowerCase().endsWith(".mp4");

        if (isVideo) {
            slideDiv.innerHTML = `
                <video src="${file}" muted loop playsinline autoplay style="width: 100%; height: 100%; object-fit: cover; cursor: pointer;" onclick="openLightbox(${index})"></video>
                <div class="slideshow-caption">Project Video ${index + 1}</div>
            `;
        } else {
            slideDiv.innerHTML = `
                <img src="${file}" alt="Project ${index + 1}" style="cursor: pointer;" onclick="openLightbox(${index})" onerror="this.src='placeholder.png'">
                <div class="slideshow-caption">Project Item ${index + 1}</div>
            `;
        }
        container.appendChild(slideDiv);
    });
}

function moveSlide(n) {
    const slides = document.getElementsByClassName("mySlides");
    if (slides.length === 0) return;

    const activeVideo = slides[slideIndex].querySelector("video");
    if (activeVideo) activeVideo.pause();

    slides[slideIndex].style.display = "none";
    slideIndex = (slideIndex + n + slides.length) % slides.length;
    slides[slideIndex].style.display = "block";

    const nextVideo = slides[slideIndex].querySelector("video");
    if (nextVideo) {
        nextVideo.currentTime = 0;
        nextVideo.play().catch(err => console.log("Autoplay paused"));
    }
}

/* ==========================================================================
   Lightbox System
   ========================================================================== */
function openLightbox(index) {
    currentLightboxIndex = parseInt(index, 10);
    const modal = document.getElementById("portfolioLightboxModal");
    if (!modal) return;
    modal.style.display = "block";
    document.body.style.overflow = "hidden";
    renderLightboxThumbnails();
    updateLightboxStage(currentLightboxIndex);
}

function closeLightbox() {
    const modal = document.getElementById("portfolioLightboxModal");
    if (!modal) return;
    modal.style.display = "none";
    document.body.style.overflow = "auto";
    const video = document.querySelector(".lightbox-main-stage video");
    if (video) video.pause();
}

function updateLightboxStage(index) {
    const stage = document.querySelector(".lightbox-main-stage");
    const caption = document.getElementById("lightboxStageCaption");
    const file = portfolioItems[index];
    const isVideo = file.toLowerCase().endsWith(".mp4");

    stage.querySelectorAll("img, video").forEach(el => el.remove());

    if (isVideo) {
        const v = document.createElement("video");
        v.src = file;
        v.controls = true;
        v.autoplay = true;
        v.style.maxWidth = "100%";
        stage.insertBefore(v, caption);
    } else {
        const i = document.createElement("img");
        i.src = file;
        i.style.maxWidth = "100%";
        i.onerror = function() { this.src = 'placeholder.png'; };
        stage.insertBefore(i, caption);
    }
    caption.innerText = `Project ${index + 1}`;
}

function renderLightboxThumbnails() {
    const tray = document.getElementById("thumbnailsTray");
    tray.innerHTML = "";
    portfolioItems.forEach((file, index) => {
        const thumb = document.createElement("img");
        thumb.className = "lightbox-thumb";
        // Inline SVG placeholder for videos
        thumb.src = file.toLowerCase().endsWith(".mp4")
            ? "data:image/svg+xml;utf8,<svg xmlns='http://www.w3.org/2000/svg' viewBox='0 0 512 512'><path fill='%23d97706' d='M0 96v320c0 35.3 28.7 64 64 64h384c35.3 0 64-28.7 64-64V96c0-35.3-28.7-64-64-64H64C28.7 32 0 60.7 0 96z'/></svg>"
            : file;
        thumb.onclick = () => { currentLightboxIndex = index; updateLightboxStage(index); };
        tray.appendChild(thumb);
    });
}

/* ==========================================================================
   Initialization
   ========================================================================== */
document.addEventListener("DOMContentLoaded", () => {
    renderSlideshow();

    // UI Controls
    document.getElementById("prevSlideBtn")?.addEventListener("click", () => moveSlide(-1));
    document.getElementById("nextSlideBtn")?.addEventListener("click", () => moveSlide(1));
    document.getElementById("viewFullGalleryBtn")?.addEventListener("click", () => openLightbox(slideIndex));
    document.getElementById("closeLightboxBtn")?.addEventListener("click", closeLightbox);

    // Lightbox Navigation
    document.getElementById("stagePrevBtn")?.addEventListener("click", () => {
        currentLightboxIndex = (currentLightboxIndex - 1 + portfolioItems.length) % portfolioItems.length;
        updateLightboxStage(currentLightboxIndex);
    });
    document.getElementById("stageNextBtn")?.addEventListener("click", () => {
        currentLightboxIndex = (currentLightboxIndex + 1) % portfolioItems.length;
        updateLightboxStage(currentLightboxIndex);
    });
});